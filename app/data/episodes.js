// Helper to get episode by slug
export function getEpisodeBySlug(slug) {
  return episodes.find(ep => ep.id === slug);
}

// Helper to get episodes by tag
export function getEpisodesByTag(tag) {
  return episodes.filter(ep => ep.tags.includes(tag));
}

// Helper to get related episodes
export function getRelatedEpisodes(episodeId) {
  const episode = getEpisodeBySlug(episodeId);
  if (!episode) return [];
  return episode.relatedEpisodes
    .map(id => getEpisodeBySlug(id))
    .filter(Boolean);
}
